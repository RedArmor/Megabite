#include "light.h"

/******************************************************************************
 * light::light(int, int, float, float, float, float, float, float)           *
 ******************************************************************************
 * Gets called when the object is first created. Sets the variables and       *
 * enables the light.                                                         *
 ******************************************************************************/
light::light(int id, int type, float red, float green, float blue, float x, float y, float z) {
    color[0] = red; color[1] = green; color[2] = blue; color[3] = 1.0;
    position[0] = x; position[1] = y; position[2] = z; position[3] = 1.0;
    this->id = id;
    this->type = type;
    
    enable();
}

/******************************************************************************
 * light::~light(void)                                                        *
 ******************************************************************************
 * Gets called when the object is destroyed. Disables the light.              *
 ******************************************************************************/
light::~light() {
    disable();
}


/******************************************************************************
 * light::enable(void)                                                        *
 ******************************************************************************
 * Enables the light.                                                         *
 ******************************************************************************/
void light::enable() {
    glEnable(16384 + id);
}


/******************************************************************************
 * light::disable(void)                                                       *
 ******************************************************************************
 * Disables the light.                                                        *
 ******************************************************************************/
void light::disable() {
    glDisable(16384 + id);
}


/******************************************************************************
 * light::setX(float)                                                         *
 ******************************************************************************
 * Sets the X coordinate of the light                                         *
 ******************************************************************************/
void light::setX(float x) {
    position[0] = x;
}

/******************************************************************************
 * light::setY(float)                                                         *
 ******************************************************************************
 * Sets the Y coordinate of the light                                         *
 ******************************************************************************/
void light::setY(float y) {
    position[1] = y;
}

/******************************************************************************
 * light::setZ(float)                                                         *
 ******************************************************************************
 * Sets the Z coordinate of the light                                         *
 ******************************************************************************/
void light::setZ(float z) {
    position[2] = z;
}

/******************************************************************************
 * light::setLocation(float, float, float)                                    *
 ******************************************************************************
 * Sets the location of the light                                             *
 ******************************************************************************/
void light::setLocation(float x, float y, float z) {
    position[0] = x;
    position[1] = y;
    position[2] = z;
}